#include <stdio.h>

int main() {

  printf("%d\n", 1 + 1);
  printf("%d\n", 1 - 1);
  printf("%d\n", 1 * 3);
  printf("%d\n", 4 / 2);
  printf("%d\n", 10 % 8);
  printf("%d\n", 11 + -1);

  printf("%.2f\n", 1.2 + 1.3);
  printf("%.2f\n", 1.2 - 1.2);
  printf("%.2f\n", 1.2 * 1.3);
  printf("%.2f\n", 3.9 / 1.3);
  printf("%.2f\n", 1.1 + -1.0);
  return 0;
}
